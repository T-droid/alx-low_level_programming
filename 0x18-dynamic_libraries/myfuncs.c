#include "main.h"

/**
 * add - sums two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of two integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subracts two integers
 * @a: first integer
 * @b: second integer
 * Return: the difference
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multipliers two integer
 * @a: first integer
 * @b: second integer
 * Return: product of two integers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - finds the modulus of two numbers
 * @a: first integer
 * @b: second integer
 * Return: the modulus
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * div - divides two integers
 * @a: first integer
 * @b: second integer
 * Return: the quotient of two nos
 */
int div(int a, int b)
{
	return (a / b);
}
